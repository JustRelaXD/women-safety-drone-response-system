CREATE TABLE IF NOT EXISTS mangalore.location_network_nodes (
    location_id bigint PRIMARY KEY,
    vertex_id bigint NOT NULL,
    road_segment_id bigint,
    original_geom geometry(Point,3857),
    snapped_geom geometry(Point,3857),
    distance_to_road double precision,
    distance_to_vertex double precision,
    updated_at timestamp without time zone DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_location_network_location
        FOREIGN KEY (location_id) REFERENCES mangalore.locations(id)
        ON DELETE CASCADE,
    CONSTRAINT fk_location_network_vertex
        FOREIGN KEY (vertex_id) REFERENCES mangalore.roads_noded_vertices_pgr(id),
    CONSTRAINT fk_location_network_road_segment
        FOREIGN KEY (road_segment_id) REFERENCES mangalore.roads_noded(id)
