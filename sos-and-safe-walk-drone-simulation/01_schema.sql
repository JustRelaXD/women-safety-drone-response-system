CREATE OR REPLACE FUNCTION mangalore.set_location_geom_3857()
RETURNS trigger
LANGUAGE plpgsql
AS $$
BEGIN
    NEW.geom := ST_Transform(
        ST_SetSRID(
            ST_MakePoint(NEW.longitude::double precision, NEW.latitude::double precision),
            4326
        ),
        3857
    );

    RETURN NEW;
END;
$$;

CREATE OR REPLACE FUNCTION mangalore.snap_location_to_network()
RETURNS trigger
LANGUAGE plpgsql
AS $$
DECLARE
    nearest_edge record;
    nearest_vertex record;
BEGIN
    IF NEW.geom IS NULL THEN
        RAISE EXCEPTION 'Location % has no EPSG:3857 geometry to snap', NEW.id;
    END IF;

    SELECT
        rn.id AS road_segment_id,
        ST_ClosestPoint(rn.geom, NEW.geom) AS snapped_geom,
        ST_Distance(NEW.geom, rn.geom) AS distance_to_road
    INTO nearest_edge
    FROM mangalore.roads_noded AS rn
    WHERE rn.geom IS NOT NULL
    ORDER BY rn.geom <-> NEW.geom
    LIMIT 1;

    IF nearest_edge.road_segment_id IS NULL THEN
        RAISE EXCEPTION 'Cannot snap location %, no mangalore.roads_noded edges found', NEW.id;
    END IF;

    SELECT
        v.id AS vertex_id,
        ST_Distance(nearest_edge.snapped_geom, v.the_geom) AS distance_to_vertex
    INTO nearest_vertex
    FROM mangalore.roads_noded_vertices_pgr AS v
    WHERE v.the_geom IS NOT NULL
    ORDER BY v.the_geom <-> nearest_edge.snapped_geom
    LIMIT 1;

    IF nearest_vertex.vertex_id IS NULL THEN
        RAISE EXCEPTION 'Cannot snap location %, no mangalore.roads_noded_vertices_pgr vertices found', NEW.id;
    END IF;

    INSERT INTO mangalore.location_network_nodes (
        location_id,
        vertex_id,
        road_segment_id,
        original_geom,
        snapped_geom,
        distance_to_road,
        distance_to_vertex,
        updated_at
    )
    VALUES (
        NEW.id,
        nearest_vertex.vertex_id,
        nearest_edge.road_segment_id,
        NEW.geom,
        nearest_edge.snapped_geom,
        nearest_edge.distance_to_road,
        nearest_vertex.distance_to_vertex,
        CURRENT_TIMESTAMP
    )
    ON CONFLICT (location_id) DO UPDATE
    SET vertex_id = EXCLUDED.vertex_id,
        road_segment_id = EXCLUDED.road_segment_id,
        original_geom = EXCLUDED.original_geom,
        snapped_geom = EXCLUDED.snapped_geom,
        distance_to_road = EXCLUDED.distance_to_road,
        distance_to_vertex = EXCLUDED.distance_to_vertex,
        updated_at = CURRENT_TIMESTAMP;

    RETURN NEW;
END;
$$;

