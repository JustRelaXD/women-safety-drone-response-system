DO $$
BEGIN
    IF EXISTS (
        SELECT 1
        FROM information_schema.columns
        WHERE table_schema = 'mangalore'
          AND table_name = 'location_network_nodes'
          AND column_name = 'distance_m'
    )
    AND NOT EXISTS (
        SELECT 1
        FROM information_schema.columns
        WHERE table_schema = 'mangalore'
          AND table_name = 'location_network_nodes'
          AND column_name = 'distance_to_road'
    ) THEN
        ALTER TABLE mangalore.location_network_nodes
            RENAME COLUMN distance_m TO distance_to_road;
