DO $$
BEGIN
    IF NOT EXISTS (
        SELECT 1
        FROM pg_attribute
        WHERE attrelid = 'mangalore.locations'::regclass
          AND attname = 'id'
          AND attidentity <> ''
    ) THEN
        ALTER TABLE mangalore.locations
            ALTER COLUMN id ADD GENERATED BY DEFAULT AS IDENTITY;
    END IF;
END;
$$;

