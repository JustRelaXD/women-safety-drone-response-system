-- Mangalore smart recycle routing schema.
--
-- Preconditions:
--   - Run against the PostgreSQL database that already contains the
--     mangalore.roads, mangalore.roads_noded, and
--     mangalore.roads_noded_vertices_pgr network tables.
--   - PostGIS and pgRouting extensions must already be installed.
--   - Routing geometries remain in EPSG:3857. App-facing latitude/longitude
--     are transformed into mangalore.locations.geom by trigger.

BEGIN;

DO $$
BEGIN
    IF NOT EXISTS (SELECT 1 FROM pg_extension WHERE extname = 'postgis') THEN
        RAISE EXCEPTION 'PostGIS extension is required before running this schema';
    END IF;

    IF NOT EXISTS (SELECT 1 FROM pg_extension WHERE extname = 'pgrouting') THEN
        RAISE EXCEPTION 'pgRouting extension is required before running this schema';
    END IF;
END;
$$;

CREATE SCHEMA IF NOT EXISTS mangalore;

DO $$
BEGIN
    IF to_regclass('mangalore.locations') IS NULL THEN
        CREATE TABLE mangalore.locations (
            id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
            name varchar(150) NOT NULL,
            location_type text NOT NULL,
            address varchar(255),
            latitude numeric(10,7) NOT NULL,
            longitude numeric(10,7) NOT NULL,
            place_query text,
            geocoding_source text,
            geocoded_at timestamptz,
            geom geometry(Point,3857),
            created_at timestamp without time zone DEFAULT CURRENT_TIMESTAMP,
            CONSTRAINT uq_location_name UNIQUE (name),
            CONSTRAINT locations_location_type_check
                CHECK (location_type = ANY (ARRAY[
                    'DEPOT'::text,
                    'BIN'::text,
                    'CUSTOMER'::text,
                    'VENDOR'::text,
                    'TRUCK_STOP'::text
                ]))
        );
    END IF;
END;
$$;

