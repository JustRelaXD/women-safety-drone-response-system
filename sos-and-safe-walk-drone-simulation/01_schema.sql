CREATE TABLE IF NOT EXISTS mangalore.routing_edges (
    id bigint PRIMARY KEY,
    old_road_id integer,
    source bigint NOT NULL,
    target bigint NOT NULL,
    length_m double precision NOT NULL,
    cost double precision NOT NULL,
    reverse_cost double precision NOT NULL,
    osm_id bigint,
    name varchar,
    highway varchar,
    oneway varchar,
    surface varchar,
    width varchar,
    geom geometry(LineString,3857) NOT NULL
);

TRUNCATE TABLE mangalore.routing_edges;

INSERT INTO mangalore.routing_edges (
    id,
    old_road_id,
    source,
    target,
    length_m,
    cost,
    reverse_cost,
    osm_id,
    name,
    highway,
    oneway,
    surface,
    width,
    geom
)
SELECT
    rn.id,
    rn.old_id,
    rn.source,
    rn.target,
    ST_Length(rn.geom) AS length_m,
    CASE
        WHEN COALESCE(NULLIF(btrim(r.oneway), ''), 'no') = '-1' THEN -1.0
        ELSE ST_Length(rn.geom)
    END AS cost,
    CASE
        WHEN COALESCE(NULLIF(btrim(r.oneway), ''), 'no') IN ('yes', 'true', '1') THEN -1.0
        ELSE ST_Length(rn.geom)
    END AS reverse_cost,
    r.osm_id,
    r.name,
    r.highway,
    r.oneway,
    r.surface,
    r.width,
    rn.geom
FROM mangalore.roads_noded AS rn
LEFT JOIN mangalore.roads AS r
    ON r.fid = rn.old_id
WHERE rn.source IS NOT NULL
  AND rn.target IS NOT NULL
  AND rn.geom IS NOT NULL;

